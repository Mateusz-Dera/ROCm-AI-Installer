# Function to display the main menu
show_menu() {
    choice=$(whiptail --title "ROCm-AI-Installer $VERSION" --menu "Choose an option:" 17 100 4 \
    1 "Variables" \
    2 "Create a container" \
    3 "Text generation" \
    --cancel-button "Exit" \
    2>&1 > /dev/tty)

    case $choice in
        1)
            variables
            ;;
        2)
            create_container
            ;;
        3)
            text_generation
            ;;
        *)
            exit 0
            ;;
    esac
}

# Variables
variables() {
    second=true
    while $second; do

        choice=$(whiptail --title "Variables" --menu "Choose an option:" 15 100 4 --cancel-button "Back" \
            "1" "GFX" \
            "2" "HSA_OVERRIDE_GFX_VERSION" \
            "3" "PATH" \
            2>&1 > /dev/tty)
        status=$?

        if [ $status -ne 0 ]; then
            return 0
        fi

        case "$choice" in
            "1")
                configure_gfx
                ;;
            "2")
                configure_hsa
                ;;
            "3")
                configure_path
                ;;
            *)
                echo "Invalid selection."
                second=false
                ;;
        esac
    done
}

# Main execution
check_whiptail
load_config

# Main loop
while show_menu; do
    :
done