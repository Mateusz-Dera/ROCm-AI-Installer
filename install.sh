# Version
VERSION="10.0"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CONFIG_FILE="${SCRIPT_DIR}/.env"

# Default values
DEFAULT_HSA_VERSION="11.0.0"
DEFAULT_GFX="gfx1100"
DEFAULT_AI_DIR="${HOME}/AI"

# Colors
export NEWT_COLORS='
